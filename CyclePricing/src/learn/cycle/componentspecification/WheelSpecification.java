package learn.cycle.componentspecification;

public class WheelSpecification {

	private int spokes;
	private int rim;
	private int tube;
	private int tyre;
	public int getSpokes() {
		return spokes;
	}
	public void setSpokes(int spokes) {
		this.spokes = spokes;
	}
	public int getRim() {
		return rim;
	}
	public void setRim(int rim) {
		this.rim = rim;
	}
	public int getTube() {
		return tube;
	}
	public void setTube(int tube) {
		this.tube = tube;
	}
	public int getTyre() {
		return tyre;
	}
	public void setTyre(int tyre) {
		this.tyre = tyre;
	}
}
