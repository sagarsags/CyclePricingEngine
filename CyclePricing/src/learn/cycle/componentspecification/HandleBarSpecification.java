package learn.cycle.componentspecification;

public class HandleBarSpecification {

	private int handleBarType;
	private int handleBarMaterial;
	private int handleBarCovering;
	
	public int getHandleBarCovering() {
		return handleBarCovering;
	}
	public void setHandleBarCovering(int handleBarCovering) {
		this.handleBarCovering = handleBarCovering;
	}
	public int getHandleBarType() {
		return handleBarType;
	}
	public void setHandleBarType(int handleBarType) {
		this.handleBarType = handleBarType;
	}
	public int getHandleBarMaterial() {
		return handleBarMaterial;
	}
	public void setHandleBarMaterial(int handleBarMaterial) {
		this.handleBarMaterial = handleBarMaterial;
	}
}
